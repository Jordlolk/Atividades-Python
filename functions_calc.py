def add(num1,num2):
    res = num1 + num2
    print("The result of calculation is: " + str(res))

def subtraction(num1 , num2):
    res = num1 - num2
    print("The result of calculation is: " + str(res))

def multiplication(num1,num2):
    res = num1*num2
    print("The result of calculation is: " + str(res))

def division(num1,num2):
    res = num1/num2
    print("The result of calculation is: " + str(res))